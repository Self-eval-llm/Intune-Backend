-- STEP 8: CREATE VIEWS FOR ANALYSIS
-- ============================================================================

-- View: Checkpoint Summary Statistics
CREATE OR REPLACE VIEW v_checkpoint_summary AS
SELECT 
    checkpoint,
    COUNT(*) as total_records,
    COUNT(*) FILTER (WHERE status IS NULL) as uninitialized,
    COUNT(*) FILTER (WHERE status = 'score') as ready_for_score,
    COUNT(*) FILTER (WHERE status = 'finetune') as ready_for_finetune,
    COUNT(*) FILTER (WHERE status = 'output_tuned') as ready_for_output,
    COUNT(*) FILTER (WHERE status = 'score_tuned') as ready_for_score_tuned,
    COUNT(*) FILTER (WHERE status = 'completed') as completed,
    ROUND(AVG(score)::numeric, 4) as avg_score_base,
    ROUND(AVG(score_tuned)::numeric, 4) as avg_score_tuned,
    ROUND(AVG(improvement)::numeric, 4) as avg_improvement,
    ROUND((AVG(improvement) / NULLIF(AVG(score), 0) * 100)::numeric, 2) as improvement_pct
FROM modelcomp_50k
GROUP BY checkpoint
ORDER BY checkpoint;

-- View: Detailed Metrics Comparison (Base vs Tuned)
CREATE OR REPLACE VIEW v_metrics_comparison AS
SELECT 
    checkpoint,
    COUNT(*) FILTER (WHERE status = 'completed') as records,
    -- Base metrics averages
    ROUND(AVG(structured_correctness)::numeric, 4) as base_struct_correct,
    ROUND(AVG(task_success)::numeric, 4) as base_task_success,
    ROUND(AVG(instruction_following)::numeric, 4) as base_instr_follow,
    ROUND(AVG(coverage)::numeric, 4) as base_coverage,
    ROUND(AVG(faithfulness)::numeric, 4) as base_faithfulness,
    ROUND(AVG(hallucination)::numeric, 4) as base_hallucination,
    -- Tuned metrics averages
    ROUND(AVG(structured_correctness_tuned)::numeric, 4) as tuned_struct_correct,
    ROUND(AVG(task_success_tuned)::numeric, 4) as tuned_task_success,
    ROUND(AVG(instruction_following_tuned)::numeric, 4) as tuned_instr_follow,
    ROUND(AVG(coverage_tuned)::numeric, 4) as tuned_coverage,
    ROUND(AVG(faithfulness_tuned)::numeric, 4) as tuned_faithfulness,
    ROUND(AVG(hallucination_tuned)::numeric, 4) as tuned_hallucination
FROM modelcomp_50k
WHERE status = 'completed'
GROUP BY checkpoint
ORDER BY checkpoint;

